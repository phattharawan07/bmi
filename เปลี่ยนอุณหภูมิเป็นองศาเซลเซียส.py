celsius = float(input("กรุณาป้อนอุณหภูมิเซลเซียส:"))
fahrenheit = (celsius * 9/5) + 32
kelvin = celsius + 273.15

print("______________________________________")
print(f"อุณหภูมิ {celsius} เซลเซียส เทียบเท่ากับ {fahrenheit}องศาฟาเรนไฮต์")
print(f"อุณหภูมิ {celsius} เซลเซียส เทียบเท่ากับ {kelvin} องศาเคลวิน")
print("______________________________________")